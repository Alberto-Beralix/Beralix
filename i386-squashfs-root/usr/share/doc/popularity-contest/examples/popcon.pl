#the catchall regexp.

for $sec (".",@dists)
{
  my @list = grep {$section{$_} =~ /^$sec/ } @pkgs;
  make ($sec, \%pkg, @list);
}
make ("maint", \%maintpkg, @maints);
make ("source", \%sourcepkg, @sources);
make ("sourcemax", \%sourcemax, @sources);

